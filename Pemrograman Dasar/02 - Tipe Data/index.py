# Tipe Data Numerik: Integer, Float, Complex
print("Tipe Data Numerik: Integer, Float, Complex")
# Integer
angka_bulat = 10
print(angka_bulat)
# Float
angka_desimal = 1.23
print(angka_desimal)
# Complex
angka_kompleks = 1 + 2j
print(angka_kompleks)

# Tipe Data String
print("\nTipe Data String")
kata = "Hello, world!"
print(kata)

# Tipe data Boolean
print("\nTipe Data Boolean")
benar = True
salah = False
print(benar)
print(salah)

# Tipe Data List
print("\nTipe Data List")
angka = [1, 2, 3]
string = ["Hello", "world!"]
boolean = [True, False]
print(angka)
print(string)
print(boolean)

# Tipe Data Tuple
print("\nTipe Data Tuple")
angka = (1, 2, 3)
string = ("Hello", "world!")
boolean = (True, False)
print(angka)
print(string)
print(boolean)

# Tipe Data Dict
print("\nTipe Data Dict")
angka = {"satu": 1, "dua": 2, "tiga": 3}
string = {"nama": "John Doe", "usia": 25}
boolean = {"benar": True, "salah": False}
print(angka)
print(string)
print(boolean)

# Tipe Data Set
print("\nTipe Data Set")
angka = {1, 2, 3}
string = {"Hello", "world!"}
boolean = {True, False}
print(angka)
print(string)
print(boolean)

# Casting Tipe Data
print("\nCasting Tipe Data")
angka_float = 3.14
angka_integer = int(angka_float)
print(angka_integer)
angka_integer = 5
angka_float = float(angka_integer)
print(angka_float)
angka = 42
teks_angka = str(angka)
print(teks_angka)